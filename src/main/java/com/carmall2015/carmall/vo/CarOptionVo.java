package com.carmall2015.carmall.vo;

public class CarOptionVo {
	private long option_no;
	private long car_no;
	private String name;
	
	public long getOption_no() {
		return option_no;
	}
	public void setOption_no(long option_no) {
		this.option_no = option_no;
	}
	public long getCar_no() {
		return car_no;
	}
	public void setCar_no(long car_no) {
		this.car_no = car_no;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	

}
